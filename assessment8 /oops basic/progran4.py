class Rectangle:
    def __init__(self, length, width):
        self.length = length
        self.width = width

    def area(self):
        return self.length * self.width

    def perimeter(self):
        return 2 * (self.length + self.width)

r = Rectangle(6, 5)

print("Area of Rectangle:", r.area())
print("Perimeter of Rectangle:", r.perimeter())
